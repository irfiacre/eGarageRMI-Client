    private void carsTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_carsTableMouseClicked
        DefaultTableModel tblModel = (DefaultTableModel) carsTable.getModel();
        int row = carsTable.getSelectedRow();
        Integer carId = Integer.parseInt(tblModel.getValueAt(row, 0).toString());
        Cars carObj = helperMethods.findCar(carId);

        this.editedCar = carObj;
        nameLabel.setText(carObj.getUsers().getFirstName() + " " + carObj.getUsers().getLastName());
        emailLabel.setText(carObj.getUsers().getEmail());
        modelLabel.setText(carObj.getModel());
        typeLabel.setText(carObj.getBrand());
        yearLabel.setText(carObj.getMakeYear());
        descriptionLabel.setText(carObj.getFaultDescription());
        statusComboBox.setSelectedItem(carObj.getStatus().toString());
    }//GEN-LAST:event_carsTableMouseClicked

