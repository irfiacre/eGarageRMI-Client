    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed

        try {

            CarFixStatus status = null;
            for (CarFixStatus value : CarFixStatus.values()) {
                if (statusComboBox.getSelectedItem().equals(value.toString())) {
                    status = value;
                }
            }
            if (this.editedCar == null) {
                JOptionPane.showMessageDialog(
                        this,
                        "No Car was selected",
                        "warning",
                        JOptionPane.WARNING_MESSAGE
                );
            } else if (status.toString().equals(this.editedCar.getStatus().toString())) {
                JOptionPane.showMessageDialog(
                        this,
                        "The status has not changed!",
                        "warning",
                        JOptionPane.WARNING_MESSAGE
                );
            } else {
                Registry theRegistry = LocateRegistry.getRegistry("127.0.0.1", SERVER_PORT);
                CarsService service = (CarsService) theRegistry.lookup("cars");

                Cars theCar = this.editedCar;

                theCar.setStatus(status);
                System.out.println(">>>>>" + theCar.getId() + " " + theCar.getStatus().toString());
                Cars carObj = service.updateCar(theCar);

                if (carObj != null) {
                    JOptionPane.showMessageDialog(this, "Car Updated Successfully");
                } else {
                    JOptionPane.showMessageDialog(this, "Car not Updated");
                }
                this.editedCar = carObj;
                view_data();
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

